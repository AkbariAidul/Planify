    // Method untuk mengambil data tugas dari Database ke Tabel
    private void tampilkanData() {
    // Model Tabel
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID");          // Kolom 0 (Hidden)
    model.addColumn("Judul");       // Kolom 1
    model.addColumn("Deadline");    // Kolom 2
    model.addColumn("Prioritas");   // Kolom 3
    model.addColumn("Status");      // Kolom 4
    model.addColumn("Deskripsi");   // Kolom 5 (Hidden baru!)
    
    tbl_tugas.setModel(model);
    
    // Sembunyikan ID (Kolom 0) DAN Deskripsi (Kolom 5) dari pandangan user
    tbl_tugas.getColumnModel().getColumn(0).setMinWidth(0);
    tbl_tugas.getColumnModel().getColumn(0).setMaxWidth(0);
    tbl_tugas.getColumnModel().getColumn(0).setWidth(0);
    
    tbl_tugas.getColumnModel().getColumn(5).setMinWidth(0);
    tbl_tugas.getColumnModel().getColumn(5).setMaxWidth(0);
    tbl_tugas.getColumnModel().getColumn(5).setWidth(0);

    try {
        java.sql.Connection conn = DatabaseConnection.connect();
        String sql = "SELECT * FROM tasks WHERE user_id = ?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, UserSession.getUserId()); 
        
        java.sql.ResultSet rs = pst.executeQuery();
        
        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getInt("id"),
                rs.getString("title"),
                rs.getDate("deadline"),
                rs.getString("priority"),
                rs.getString("status"),
                rs.getString("description") // Ambil deskripsi juga
            });
        }
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
}
    
    private void kosongkanForm() {
    // Mengosongkan semua kotak input
    txt_judul.setText("");
    txt_deadline.setText("");
    cmb_prioritas.setSelectedIndex(0); // Kembali ke pilihan pertama
    txt_deskripsi.setText("");
    
    // Menghilangkan sorotan (seleksi) pada tabel
    tbl_tugas.clearSelection();
}
    

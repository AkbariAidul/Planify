        // Ambil data dari tabel (Ingat urutan kolom di tampilkanData tadi)
        String judul = tbl_tugas.getValueAt(baris, 1).toString();
        String deadline = tbl_tugas.getValueAt(baris, 2).toString();
        String prioritas = tbl_tugas.getValueAt(baris, 3).toString();
